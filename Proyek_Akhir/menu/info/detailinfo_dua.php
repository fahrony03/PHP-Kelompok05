                    <h3> Jenis Tomat</h3>
                    <p>Tomat (Lycopersico esculentum Mill atau yang dahulu disebut solanum lycopersicum) memiliki berbagai jenis (subpesies). Beberapa subpesies yang 
                    terkenal diantaranya tomat apel, tomat perselin, tomat sayur, tomat kentang, dan tomat keriting.</p>
                    <p>1) Tomat apel – (Solanum lycopersicum pyriforme ) berbuah dengan bentuk bulat, kuat (kompak), dan sedikit keras seperti buah apel atau pir.</p>
                    <p>2) Tomat perselin atau tomat sayur – (solanum lycopersicum L commune) berbuah bulat pipih, lunak, bentuk tidak teratur, dan sedikit beralur-alur di 
                       dekat tangkainya. Tomat jenis inilah yang banyak dijual dipasar.</p> 
                    <p>3) Tomat kentang – (Solanum lycopersicum L grandifolium) berbuah bulat besar dan padat (kompak) seperti apel, hanya ukurannya lebih kecil dari 
                       tomat apel, sedangkan daunnya lebat.</p> 
                    
                    <h3>Budidaya Tomat</h3>
                    Tomat dapat tumbuh baik di dataran rendah maupun dataran tinggi (pegunungan). Jenis tomat sayar lebih baik ditanam di dataran rendah.  
                    Sementara itu, tomat apel lebih baik ditanam di dataran tinggi. namun, ada variates yang termasuk tipe tomat apel yang cocok ditanam di 
                    dataran rendah dan tahan terhadap penyakit layu seperti VC.11 (ratna), AV-33 (intan), berlian, mutiara, dan TW 375.
                    Tanaman tomat sangat peka terhadap tanah yang sedikit kekuarangan zat zat hara, terutama unsur nitrogen (zat lemas). Oleh karena itu, 
                    penanaman tomat harus pada tanah gembur, sedikit mengandung pasir, dan banyak mengandung bahan organik (subur). Tanah liat yang sedikit 
                    mengandung pasir dengan derajat keasaman tanah (pH) antara 5 – 6 sangat disukai tanaman ini.
                    Tanaman tomat pun tidak tahan terhadap hujan. Oleh karena itu, waktu tanam terbaik ialah dua bulan sebelum musim hujan hingga akhir musim 
                    hujan. Waktu tanam pun dapat dilakukan pada awal musim hujan. Akan tetapi, tanaman sering mengalami kegagalan karena banyak terjadi serangan 
                    penyakit daun dan buahnya banyak yang pecah sehingga mutunya menurun. Disawah atau tempat yang dapat diairi atau digenangi, waktu tanam yang 
                    paling baik adalah awal musim kemarau.

