                    <p>
                    Tomat termasuk dalam tanaman Solanacea atau terung terungan dicirikan dengan batang dan daunnya yang berbulu halus sampai kasar. 
                    Tanaman ini umumnya berdaun lebar dan yang terdapat pada tangkai bersama (majemuk). Daunnya ada yang bercagak menyirip, adapula
                    yang tidak. Bunga tanaman solanaceae berbentuk bintang atau terompet. Benang sarinya saling berlekatan atau lepas yang berbentuk kotak 
                    panjang. Mahkota bunganya ada yang berwarna kuning, ungu, putih, atau putih keunguan. Bentuk buahnya bulat seperti bola lampu,
                    lonjong atau panjang. Buah yang mudah beracun mengandung alkoloida. Tanaman berakar tunggang dengan banyak akar samping.
                    Sayuran solanaceae meliputi beberapa genus yang penting seperti tomat, kentang, terung, cabai dan ranti (leunca) serta tekokak.
                    Tomat termasuk sayuran buah yang paling digemari oleh setiap orang karena rasanya enak, segar, dan sedikit asem. Selain itu, tomat yang 
                    telah tua dan berwarna merah merupakan sumber vitamin A, vitamin C dan vitamin B. Kandungan vitamin A –nya lebih tinggi 2 – 3 semangka.
                    Kali ini Si PEKA akan memberikan uraian lengkap mengenai cara menanam wortel yang baik dan benar.
